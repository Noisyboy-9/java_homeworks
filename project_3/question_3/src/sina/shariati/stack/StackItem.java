package sina.shariati.stack;

/**
 * The type Stack item.
 */
public class StackItem {
    private final int value;

    /**
     * Instantiates a new Stack item.
     *
     * @param value the value
     */
    public StackItem(int value) {
        this.value = value;
    }

    /**
     * Gets value.
     *
     * @return the value
     */
    public int getValue() {
        return value;
    }
}
